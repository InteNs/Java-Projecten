
public class Kogel {
	private double x, y, dx;
	
	public Kogel(double x, double y, double dx)
	{
		super();
		this.x = x;
		this.y = y;
		this.dx = dx;
	}

	public double getX()
	{
		return x;
	}

	public void setX(double x)
	{
		this.x = x;
	}

	public double getY()
	{
		return y;
	}

	public void setY(double y)
	{
		this.y = y;
	}

	public double getDx() {
		return dx;
	}

	public void setDx(double dx) {
		this.dx = dx;
	}


}
